var UUID_RE = /^[0-9a-f]{8}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{12}$/;

function validUuid(uuid) {
    return typeof (uuid) === 'string' && UUID_RE.test(uuid);
}

module.exports = {
    validUuid: validUuid
};